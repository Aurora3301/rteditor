/** Search emoji by matching against category name or emoji character */
export function searchEmoji(query: string, categories: EmojiCategory[] = emojiCategories): string[] {
  if (!query.trim()) return []
  const lowerQuery = query.toLowerCase()
  const results: string[] = []
  for (const category of categories) {
    if (category.name.toLowerCase().includes(lowerQuery)) {
      results.push(...category.emojis)
    }
  }
  // Also do a direct match for emoji characters
  if (results.length === 0) {
    for (const category of categories) {
      for (const emoji of category.emojis) {
        if (emoji.includes(query)) {
          results.push(emoji)
        }
      }
    }
  }
  return [...new Set(results)]
}
